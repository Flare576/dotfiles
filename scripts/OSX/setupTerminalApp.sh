
# Adjust Terminal settings
osascript -e 'tell application "Terminal" to set the font name of window 1 to "JetBrains Mono Regular"'
osascript -e 'tell application "Terminal" to set the font size of window 1 to 14'
osascript -e 'tell application "Terminal" to set normal text color of window 1 to {65535, 65535, 65535}'
osascript -e 'tell application "Terminal" to set background color of window 1 to {0, 0, 0}'
